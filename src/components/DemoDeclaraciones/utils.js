function getAll(obj, base) {
    for (let i in obj) {
       if (typeof  obj[i] === 'object') {
            if(Array.isArray(obj[i])){
                let index = 0;
                for(let j in obj[i]){
                   base[i][index]= getAll(obj[i][j],Object.assign({},base[i][0]));
                    index +=1;
                }
            }else if(base[i]){
                getAll(obj[i], base[i]);
            }
        }
        else {
            base[i] = obj[i];
        }
    }
    return base;
}
